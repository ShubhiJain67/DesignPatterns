module SingletonDesignPattern {
	/* Basic Overview:
	 * Creational Design Pattern
	 * Only One instance of class should exist throughout the implementation
	 * Every class should be able to get instance of the singleton class
	 * Used in Logging, Cache, Session, Drivers
	 * */
	
	/* Implementation:
	 * Constructor should be private so that no class can directly create the instance
	 * We need to have a method which returns the instance to the class
	 * the instance type should be public and static
	 * */
	
	/* Initialization Types:
	 * Eager Initialization
	 * Lazy Initialization
	 * Thread Safe Method Initialization
	 * Thread Safe Block Initialization
	 * */
}